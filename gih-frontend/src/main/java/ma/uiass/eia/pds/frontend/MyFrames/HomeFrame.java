    private void loadChambres(DefaultTableModel tableModel) {

        tableModel.setRowCount(0);
        for (Chambre chambre : chambres) {
            Object[] row = new Object[]{chambre.getCode(), chambre.getType()};
            tableModel.addRow(row);
        }
    }

